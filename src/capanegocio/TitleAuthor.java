
package capanegocio;

public class TitleAuthor {
    
    private Author author;
    private Title title;
    private int arOrd;
    private int royalType;

    public Author getAuthor() {
        return author;
    }

    public void setAuthor(Author author) {
        this.author = author;
    }

    public Title getTitle() {
        return title;
    }

    public void setTitle(Title title) {
        this.title = title;
    }

    public int getArOrd() {
        return arOrd;
    }

    public void setArOrd(int arOrd) {
        this.arOrd = arOrd;
    }

    public int getRoyalType() {
        return royalType;
    }

    public void setRoyalType(int royalType) {
        this.royalType = royalType;
    }
    
}
